def auth_headers(workspace_id="ws_1", user_id="user_alice", permissions=None):
    if permissions is None:
        permissions = ["approval:read", "approval:create", "approval:decide", "approval:cancel"]
    return {
        "X-Workspace-Id": workspace_id,
        "X-User-Id": user_id,
        "X-Permissions": ",".join(permissions),
    }